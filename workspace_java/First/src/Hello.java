//공통사용 hello라는 class사용한다
public class Hello {
    //static이붙은걸 모두찾아 main안에불러간다
    //public static boid main(Stfing[] args) 는 외워야함
    //ㄴ 메인은 하나의 형식바께 없엉 외워야행
    //main쓰고 enter치면됨 main메서드의 위에서 아래로 해ㅓㅅㄱ한다
    public static void main(String[] args) {
        //시스템에 출력한다 "헬로월드"라고
        System.out.println("Hello, World");
    }

}
